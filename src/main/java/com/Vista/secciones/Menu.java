    private void btnExplorar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExplorar1ActionPerformed
        JFileChooser filechooser = new JFileChooser();
        int result = filechooser.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            archivoseleccionado = filechooser.getSelectedFile();
            txtUrlImagen1.setText(archivoseleccionado.getName());

            try {
                Image img = ImageIO.read(archivoseleccionado);
                ImageIcon originalIcon = new ImageIcon(img);
                int ancho = jlImagen1.getWidth();
                int altura = jlImagen1.getHeight();
                Image ImagenEscalada = originalIcon.getImage().getScaledInstance(ancho, altura, Image.SCALE_SMOOTH);
                jlImagen1.setIcon(new ImageIcon(ImagenEscalada));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al mostrar imagen");
            }
        }
    }//GEN-LAST:event_btnExplorar1ActionPerformed

