    public void mostrarDatoFormulario(String codigo, String nombre, float precio, int categoria, ImageIcon imagen) {
        txtCodigo.setText(codigo);
        txtNombre1.setText(nombre);
        txtPrecio1.setText(String.valueOf(precio));
        
        for (int i = 0; i < CbCategoria1.getItemCount(); i++) {
            if (CbCategoria1.getItemAt(i).toString().equals(String.valueOf(categoria))) {
                CbCategoria1.setSelectedIndex(i);
                break;
            }
        }

        jlImagen1.setIcon(imagen);
        FormullarioModificar.setBackground(new Color(230, 230, 230, 240));
        PanelCubierta.setVisible(true);
        PanelCubierta.removeAll();
        PanelCubierta.repaint();
        PanelCubierta.revalidate();
        PanelCubierta.add(FormullarioModificar);
        PanelCubierta.repaint();
        PanelCubierta.revalidate();

    }
    
